let img = document.querySelector('.img')

window.addEventListener('scroll', ()=>{
    const scrolled = window.scrollY;

    if(scrolled){
        img.style.position = 'fixed';
        img.style.overflow = "hidden"
        img.style.top = `20%`;
        img.style.right = '0';
    }
} )

